#!/bin/bash

function banner() {
  echo ===============================================================================
  echo $1
  echo ===============================================================================
}

function printBannerAndWait() {
  echo ===============================================================================
  echo $1
  echo Press ENTER to continue, Ctrl-C to abort.
  echo ===============================================================================
  read
}

function printTree() {
  find $1 | sed -e "s/[^-][^\/]*\//  |/g" -e "s/|\([^ ]\)/|-\1/"
}
